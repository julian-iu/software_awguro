    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed

        if (txtDOC.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite N°Documento");
            txtDOC.requestFocus();
            return;
        }

        if (txtNOMBRES.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Nombres");
            txtNOMBRES.requestFocus();
            return;
        }

        if (txtAPELLIDOS.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Apellidos");
            txtAPELLIDOS.requestFocus();
            return;
        }

        if (txtSEXO.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Sexo");
            txtSEXO.requestFocus();
            return;
        }

        if (txtDIRECCION.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Direccion");
            txtDIRECCION.requestFocus();
            return;
        }
        if (txtTELEFONO.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Telefono");
            txtTELEFONO.requestFocus();
            return;
        }
         if (txtFECHA.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Fecha nacimiento");
            txtFECHA.requestFocus();
            return;
        }
         if (txtESTADO.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Estado civil");
            txtESTADO.requestFocus();
            return;
        }
        try {

            Funcionario funcionario = new Funcionario();
            funcionario.setNumero_identificacion(txtDOC.getText().trim());
            funcionario.setNombres(txtNOMBRES.getText().trim());
            funcionario.setApellidos(txtAPELLIDOS.getText().trim());
            funcionario.setSexo(txtSEXO.getText().trim());
            funcionario.setDireccion(txtDIRECCION.getText().trim());
            funcionario.setTelefono(txtTELEFONO.getText().trim());
            funcionario.setFecha_nacimiento(txtFECHA.getText().trim());
            funcionario.setEstado_civil(txtESTADO.getText().trim());
            funcionarioController.crear(funcionario);
            txtDOC.setText("");
            txtNOMBRES.setText("");
            txtAPELLIDOS.setText("");
            txtSEXO.setText("");
            txtDIRECCION.setText("");
            txtTELEFONO.setText("");
            txtFECHA.setText("");
            txtESTADO.setText("");
            listFUNC();
            JOptionPane.showMessageDialog(null, "Funcionario creado con éxito");
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "No se pudo crear el Funcionario");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

