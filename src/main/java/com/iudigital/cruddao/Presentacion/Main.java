    private void listFUNC() {
        cbxFunc.removeAllItems();
        Funcionario FuncionarioCombo =  new Funcionario();
        FuncionarioCombo.setNumero_identificacion(SELECCIONE);
        FuncionarioCombo.setNombres("");
        cbxFunc.addItem(FuncionarioCombo);
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        for (String COLUMN : COLUMNS) {
            defaultTableModel.addColumn(COLUMN);
        }
        
        tblFunci.setModel(defaultTableModel);
        try {
            List<Funcionario> funcionarios = funcionarioController.obtenerFuncionarios();
            if (funcionarios.isEmpty()) {
                return;
            }
            defaultTableModel.setRowCount(funcionarios.size());
            int row = 0;
            for (Funcionario funcionario : funcionarios) {
                defaultTableModel.setValueAt(funcionario.getFuncionario_id(), row, 0);
                defaultTableModel.setValueAt(funcionario.getNumero_identificacion(), row, 1);
                defaultTableModel.setValueAt(funcionario.getNombres(), row, 2);
                defaultTableModel.setValueAt(funcionario.getApellidos(), row, 3);
                defaultTableModel.setValueAt(funcionario.getSexo(), row, 4);
                defaultTableModel.setValueAt(funcionario.getDireccion(), row, 5);
                defaultTableModel.setValueAt(funcionario.getTelefono(), row, 6);
                defaultTableModel.setValueAt(funcionario.getFecha_nacimiento(), row, 7);
                defaultTableModel.setValueAt(funcionario.getEstado_civil(), row, 8);
                row++;
                
                cbxFunc.addItem(funcionario);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    private void addListener() {
        cbxFunc.addItemListener(event -> {
             Funcionario selectedFUN = (Funcionario) event.getItem();
            if (selectedFUN.getNumero_identificacion().equals(SELECCIONE)) {
                txtFunId.setText("");
                txtDOCEdit.setText("");
                txtNOMBRESEdit.setText("");
                txtAPELLIDOSEdit.setText("");
                txtSEXOEdit.setText("");
                txtDIRECCIONEdit.setText("");
                txtTELEFONOEdit.setText("");
                txtFECHAEdit.setText("");
                txtESTADOEdit.setText("");
            } else {
                txtFunId.setText(String.valueOf(selectedFUN.getFuncionario_id()));
                txtDOCEdit.setText(selectedFUN.getNumero_identificacion());
                txtNOMBRESEdit.setText(selectedFUN.getNombres());
                txtAPELLIDOSEdit.setText(selectedFUN.getApellidos());
                txtSEXOEdit.setText(selectedFUN.getSexo());
                txtDIRECCIONEdit.setText(selectedFUN.getDireccion());
                txtTELEFONOEdit.setText(selectedFUN.getTelefono());
                txtFECHAEdit.setText(selectedFUN.getFecha_nacimiento());
                txtESTADOEdit.setText(selectedFUN.getEstado_civil());
            }
        });
    }

